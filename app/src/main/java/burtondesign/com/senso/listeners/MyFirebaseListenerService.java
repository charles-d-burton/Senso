    @Override
    public void onMessageReceived(RemoteMessage remoteMessage) {
        Log.d(TAG, "From: " + remoteMessage.getFrom());
        Log.d(TAG, "From: " + remoteMessage.getData().toString());
        Intent intent = new Intent();
        intent.putExtra("data", remoteMessage.getData().get("data"));
        intent.putExtra("sensor", remoteMessage.getData().get("sensor"));
        intent.setAction("burtodesign.com.senso.onMessageRecieved");
        sendBroadcast(intent);
        //Log.d(TAG, "Body: " + remoteMessage.getNotification().getBody());
    }
